$(document).ready(function () {
	$(document).on('click', '.modal-close', function () {
		$('#gift-modal').modal('hide')
	})

	let modalChecker = Cookies.get('modalChecker')

	if (!modalChecker) {
		Cookies.set('modalChecker', 1, {
			expires: 7,
			path: '/',
			secure: false,
		})

		modalChecker = Cookies.get('modalChecker')
	}

	switch (modalChecker) {
		case '1':
		case 1: {
			setTimeout(function () {
				$('#gift-modal').modal('show')
			}, 10000)

			break
		}

		case '2':
		case 2: {
			setTimeout(function () {
				$('#gift-modal').modal('show')
			}, 60000)

			break
		}
	}

	Cookies.set('modalChecker', parseInt(modalChecker) + 1)
})
